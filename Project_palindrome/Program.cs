using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Project_palindrome
{
    class Program
    {
        static void Main(string[] args)
        {
            int i = 0, j = 0, k = 0;

            for (int i = 0, int j = 0, k = 0; i <= 9, j <= 9, k <= 9; i++, j++, k++)
            {
                Console.Write("{0,0,0,}", i, j, k);
                Console.Read();
            }
        }
    }
}
